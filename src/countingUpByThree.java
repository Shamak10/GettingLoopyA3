public class countingUpByThree {
    public static void main(String[] args) {
        System.out.println(""); // prints a blank line
        for (int i = 0; i <= 18; i += 3) { // counts from 0 to 18 in increments of 3
            {
                System.out.println(i + " "); // prints the current count followed by a space
            }
        }
    }
}
